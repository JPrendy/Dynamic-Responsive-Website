  <p class="titleKP" id="whatWeDo">WHO WE ARE: </p>
                <p>We are a Garda Youth Diversion Project within the  Youth Project, called the KEEP project (Konnecting Empowering Every Person).</p>
                <p>We are a team of three Youth Justice Workers who work with young people aged between 12 and 18 years.</p>
                <p>Youth Justice Worker’s develop positive relationships with young people to enable them to make informed choices for themselves and to move away from criminal or anti-social behaviour.  This is done through informal youth work methods. Young people have opportunities to work in one to ones or group work sessions.</p>
                <p>We feel passionate about young people having a voice in their community & actively engaging in positive change.</p>


			          <p class="titleKP" >WHAT WE DO: </p>
			          <p>As Justice Workers we complete a need assessment for each young person to focus our core work around developing young people and supporting them in making more informed decisions. This process addresses 8 different areas of a young person’s life.  Identifying risks and reducing potential offending behaviours is core to the work of the KEEP project.</p> 

                      


<p><img class="imageKP" src="images/staff08.jpg" width="300" alt="image" /></p>


                <p>Youth Justice Workers develop positive relationships with young people through structured groups, activities and one to ones. We work in partnership with young people to develop programmes that are of interest to them.</p>				  
                <p>We help young people to recognize their ability, build their confidence and most of all think before they act.</p>
                <p class="titleKP">WHY REFER: </p>
                <p>A young person would be referred to the project as they are at risk of offending or have committed to an offence. Young people may also have additional needs which the project would be able to support.</p>

